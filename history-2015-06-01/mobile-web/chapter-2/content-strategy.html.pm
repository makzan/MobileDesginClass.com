The next most important thing after document title is our headings.

Headings define the document outline. It shows the architecture of the essay.

Correct use of headings helps both readers and software process the content.

Our main heading (h1) may be used in many places. It could be the title of our essay that’s displayed in ◊em{email newsletter} and ◊em{read later service}.

For detail about how we should use the headings, we may check the ◊link["http://www.w3.org/html/wg/drafts/html/master/sections.html#the-h1,-h2,-h3,-h4,-h5,-and-h6-elements"]{reference to the W3C spec about the headings}.

