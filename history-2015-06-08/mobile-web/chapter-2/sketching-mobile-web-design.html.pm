◊headline{Sketching mobile web design}

For sketching the layout of the mobile web, we can use a draft paper with narrow boundary box. Zurb’s sketchsheet is one of my favorites. Zurb Sketch sheet is a collection of website sketching toolkit. It contains different kinds of sketch sheets that we can use.

◊figure["http://makzan.net/assets/mobile-web-design/zurb-sketchsheet-9bf1ede200470144d65533adf69cc62b.png"]{Zurb sketch sheet}

For example, this one contains both wide and narrow boundary that we can draw our website layout in both wide and small screen.

◊section{Exercise time}

Now, browse to the the ◊link["http://portal.gov.mo/web/guest/citizen"]{Macao Government Portal}. Follow the website layout and draw a rough layout sketch in the wide screen on the left of the sketch sheet.

What I want you to do is then think how you would layout the content in the narrow screen. Try to draw your design on the right side.

