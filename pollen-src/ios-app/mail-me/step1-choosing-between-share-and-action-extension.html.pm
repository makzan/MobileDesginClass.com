◊headline{Choosing between share and action extension}

Sometimes the ◊em{share} and ◊em{action} extension confuse us. There are subtle difference between share and action extension.

◊em{Action Extension} is for manipulating the input items and return the output. Link a function in programming language, or a service in Mac OS X.

◊em{Share Extension} is for sending out the input items.