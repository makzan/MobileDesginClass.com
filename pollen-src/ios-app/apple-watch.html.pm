◊headline{Apple Watch}

What we can do on ◊em{Apple Watch}?

Basically 3 thing:

- Notification
- Glimpse
- App

◊section["notification"]{Notification}

The iOS automatically routes all notifications to the watch, even that app hasn’t added any Apple Watch specific functionality.

◊section["glimpse"]{Glimpse}

A glimpse is a non-interaction view that provides essential information for the watch user.

◊section["app"]{App}

App is like entension of the iPhone app, where we call it ◊strong{master app}.
