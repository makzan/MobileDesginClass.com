◊headline{Pragma mark in swift}

The Pragma Mark in Objective-C:

◊objc{
#pragma mark
}

The Pragma Mark in Swift:

◊swift{
// MARK:
}

For example, the horizontal line can be presented by:

◊swift{
// MARK: -
}

Title following an horizontal line would be:

◊swift{
// MARK: - Text Goes Here
}
